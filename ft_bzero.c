#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*z;

	i = 0;
	z = (unsigned char *) s;
	while (i < n)
	{
		z[i] = 0;
		i++;
	}
}
